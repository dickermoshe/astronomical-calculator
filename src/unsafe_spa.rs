#[no_mangle]
pub unsafe extern "C" fn EoT(
    mut lat: ::core::ffi::c_double,
    mut JD: JulianDay,
    mut GP: GeoCentricSolPos,
) -> ::core::ffi::c_double {
    let mut M: ::core::ffi::c_double = 0.;
    let mut E: ::core::ffi::c_double = 0.;
    let mut dtau: ::core::ffi::c_double = 0.;
    let mut lambda: ::core::ffi::c_double = 0.;
    let mut alpha: ::core::ffi::c_double = 0.;
    let mut eps: ::core::ffi::c_double = 0.;
    let mut deps: ::core::ffi::c_double = 0.;
    let mut dpsi: ::core::ffi::c_double = 0.;
    dtau = M_PI * (-20.4898f64 / 3600.0f64) / 180.0f64 / GP.rad;
    Nutation_lon_obliquity(JD, &raw mut dpsi, &raw mut deps);
    eps = deps
        + poly(
            &raw const ECLIPTIC_MEAN_OBLIQUITY as *const ::core::ffi::c_double,
            11 as ::core::ffi::c_int,
            JD.JME / 10 as ::core::ffi::c_int as ::core::ffi::c_double,
        ) * (M_PI * (1 as ::core::ffi::c_int as ::core::ffi::c_double / 3600.0f64) / 180.0f64);
    lambda = GP.lon + dpsi + dtau;
    alpha = atan2(sin(lambda) * cos(eps) - tan(GP.lat) * sin(eps), cos(lambda));
    M = poly(
        &raw const SMLON as *const ::core::ffi::c_double,
        6 as ::core::ffi::c_int,
        JD.JME,
    );
    E = fmod(
        M - M_PI * 0.0057183f64 / 180.0f64 - alpha + dpsi * cos(eps),
        2 as ::core::ffi::c_int as ::core::ffi::c_double * M_PI,
    );
    if E > M_PI * 5 as ::core::ffi::c_int as ::core::ffi::c_double / 180.0f64 {
        E -= 2 as ::core::ffi::c_int as ::core::ffi::c_double * M_PI;
    }
    if E < -(M_PI * 5 as ::core::ffi::c_int as ::core::ffi::c_double / 180.0f64) {
        E += 2 as ::core::ffi::c_int as ::core::ffi::c_double * M_PI;
    }
    return E;
}
#[no_mangle]
pub unsafe extern "C" fn InputCheck(
    mut delta_ut1: ::core::ffi::c_double,
    mut lon: ::core::ffi::c_double,
    mut lat: ::core::ffi::c_double,
    mut e: ::core::ffi::c_double,
    mut p: ::core::ffi::c_double,
    mut T: ::core::ffi::c_double,
) -> ::core::ffi::c_int {
    let mut E: ::core::ffi::c_int = 0 as ::core::ffi::c_int;
    if delta_ut1 < -(1 as ::core::ffi::c_int) as ::core::ffi::c_double
        || delta_ut1 > 1 as ::core::ffi::c_int as ::core::ffi::c_double
    {
        E |= _FREESPA_DEU_OOR;
    }
    if lon < -M_PI || lon > M_PI {
        E |= _FREESPA_LON_OOR;
    }
    if lat < -M_PI / 2 as ::core::ffi::c_int as ::core::ffi::c_double
        || lat > M_PI / 2 as ::core::ffi::c_int as ::core::ffi::c_double
    {
        E |= _FREESPA_LAT_OOR;
    }
    if e < -EARTH_R {
        E |= _FREESPA_ELE_OOR;
    }
    if p < 0 as ::core::ffi::c_int as ::core::ffi::c_double || p > 5000 as ::core::ffi::c_int as ::core::ffi::c_double {
        E |= _FREESPA_PRE_OOR;
    }
    if T < ABSOLUTEZERO {
        E |= _FREESPA_TEM_OOR;
    }
    return E;
}
