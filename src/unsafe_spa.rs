#[no_mangle]
pub unsafe extern "C" fn solpos(
    mut lon: ::core::ffi::c_double,
    mut lat: ::core::ffi::c_double,
    mut e: ::core::ffi::c_double,
    mut JD: JulianDay,
    mut GP: GeoCentricSolPos,
) -> sol_pos {
    let mut dtau: ::core::ffi::c_double = 0.;
    let mut v: ::core::ffi::c_double = 0.;
    let mut H: ::core::ffi::c_double = 0.;
    let mut u: ::core::ffi::c_double = 0.;
    let mut x: ::core::ffi::c_double = 0.;
    let mut y: ::core::ffi::c_double = 0.;
    let mut lambda: ::core::ffi::c_double = 0.;
    let mut alpha: ::core::ffi::c_double = 0.;
    let mut delta: ::core::ffi::c_double = 0.;
    let mut xi: ::core::ffi::c_double = 0.;
    let mut delta_prime: ::core::ffi::c_double = 0.;
    let mut H_prime: ::core::ffi::c_double = 0.;
    let mut dpsi: ::core::ffi::c_double = 0.;
    let mut deps: ::core::ffi::c_double = 0.;
    let mut eps: ::core::ffi::c_double = 0.;
    let mut dalpha: ::core::ffi::c_double = 0.;
    let mut h: ::core::ffi::c_double = 0.;
    let mut P: sol_pos = sol_pos { z: 0., a: 0., E: 0 };
    dtau = M_PI * (-20.4898f64 / 3600.0f64) / 180.0f64 / GP.rad;
    Nutation_lon_obliquity(JD, &raw mut dpsi, &raw mut deps);
    eps = deps
        + poly(
            &raw const ECLIPTIC_MEAN_OBLIQUITY as *const ::core::ffi::c_double,
            11 as ::core::ffi::c_int,
            JD.JME / 10 as ::core::ffi::c_int as ::core::ffi::c_double,
        ) * (M_PI * (1 as ::core::ffi::c_int as ::core::ffi::c_double / 3600.0f64) / 180.0f64);
    lambda = GP.lon + dpsi + dtau;
    v = poly(
        &raw const GSTA as *const ::core::ffi::c_double,
        4 as ::core::ffi::c_int,
        JD.JC,
    ) + M_PI * 360.98564736629f64 / 180.0f64 * (JD.JD - JD0);
    v += dpsi * cos(eps);
    alpha = atan2(sin(lambda) * cos(eps) - tan(GP.lat) * sin(eps), cos(lambda));
    if alpha < 0 as ::core::ffi::c_int as ::core::ffi::c_double {
        alpha += 2 as ::core::ffi::c_int as ::core::ffi::c_double * M_PI;
    }
    delta = asin(sin(GP.lat) * cos(eps) + cos(GP.lat) * sin(eps) * sin(lambda));
    H = v + lon - alpha;
    xi = M_PI * (8.794f64 / 3600.0f64) / 180.0f64 / GP.rad;
    u = atan(0.99664719f64 * tan(lat));
    x = cos(u) + e * cos(lat) / EARTH_R;
    y = 0.99664719f64 * sin(u) + e * sin(lat) / EARTH_R;
    dalpha = atan2(-x * sin(xi) * sin(H), cos(delta) - x * sin(xi) * cos(H));
    delta_prime = atan2(
        (sin(delta) - y * sin(xi)) * cos(dalpha),
        cos(delta) - x * sin(xi) * cos(H),
    );
    H_prime = H - dalpha;
    h = asin(sin(lat) * sin(delta_prime) + cos(lat) * cos(delta_prime) * cos(H_prime));
    P.z = M_PI / 2 as ::core::ffi::c_int as ::core::ffi::c_double - h;
    P.a = fmod(
        M_PI + atan2(sin(H_prime), cos(H_prime) * sin(lat) - tan(delta_prime) * cos(lat)),
        2 as ::core::ffi::c_int as ::core::ffi::c_double * M_PI,
    );
    P.z = fmod(P.z, 2 as ::core::ffi::c_int as ::core::ffi::c_double * M_PI);
    if P.z < 0 as ::core::ffi::c_int as ::core::ffi::c_double {
        P.z = -P.z;
        P.a = fmod(P.a + M_PI, 2 as ::core::ffi::c_int as ::core::ffi::c_double * M_PI);
    }
    if P.z > M_PI {
        P.z = 2 as ::core::ffi::c_int as ::core::ffi::c_double * M_PI - P.z;
        P.a = fmod(
            P.a + 2 as ::core::ffi::c_int as ::core::ffi::c_double * M_PI,
            2 as ::core::ffi::c_int as ::core::ffi::c_double * M_PI,
        );
    }
    return P;
}
