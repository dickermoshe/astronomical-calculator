#[no_mangle]
pub unsafe extern "C" fn TrueSolarTime(
    mut ut: *mut tm,
    mut delta_t: *mut ::core::ffi::c_double,
    mut delta_ut1: ::core::ffi::c_double,
    mut lon: ::core::ffi::c_double,
    mut lat: ::core::ffi::c_double,
) -> tm {
    let mut E: ::core::ffi::c_double = 0.;
    let mut nt: tm = tm { timestamp: 0 };
    let mut D: JulianDay = JulianDate {
        JD: 0.,
        JDE: 0.,
        JC: 0.,
        JCE: 0.,
        JME: 0.,
        E: 0,
    };
    let mut G: GeoCentricSolPos = GeoCentricSolPos {
        lat: 0.,
        lon: 0.,
        rad: 0.,
    };
    if InputCheck(
        delta_ut1,
        lon,
        lat,
        0 as ::core::ffi::c_int as ::core::ffi::c_double,
        1 as ::core::ffi::c_int as ::core::ffi::c_double,
        10 as ::core::ffi::c_int as ::core::ffi::c_double,
    ) != 0
    {
        return nt;
    }
    D = MakeJulianDay(ut, delta_t, delta_ut1);
    G = Geocentric_pos(D);
    E = EoT(lat, D, G);
    D.JD += (lon + E) / M_PI / 2 as ::core::ffi::c_int as ::core::ffi::c_double;
    JDgmtime(D, &raw mut nt);
    return nt;
}
